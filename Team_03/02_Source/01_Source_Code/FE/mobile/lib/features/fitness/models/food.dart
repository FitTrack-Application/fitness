class Food {
  final String id;
  final String name;
  final double servingSize;
  final double calories;
  final double carbs;
  final double fat;
  final double protein;
  final String unit;
  final String description;

  Food({
    required this.id,
    required this.name,
    required this.servingSize,
    required this.calories,
    required this.carbs,
    required this.fat,
    required this.protein,
    required this.unit,
    required this.description,
  });
}