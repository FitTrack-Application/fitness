class Food {
  final int id;
  final String name;
  final double servingSize;
  final int calories;
  final int carbs;
  final int fat;
  final int protein;
  final String unit;

  Food({
    required this.id,
    required this.name,
    required this.servingSize,
    required this.calories,
    required this.carbs,
    required this.fat,
    required this.protein,
    required this.unit,
  });
}